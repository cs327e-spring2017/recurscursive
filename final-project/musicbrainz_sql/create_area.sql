create table Area(
   id integer,
   gid varchar(40),
   name varchar(10),
   type boolean,
   edits_pending boolean,
   last_updated decimal(29,3),
   begin_date_year varchar(10),
   begin_date_month varchar(10),
   begin_date_day varchar(10),
   end_date_year varchar(10),
   end_date_month varchar(10),
   end_date_day varchar(10),
   ended varchar(10),
   comment varchar(0)
);